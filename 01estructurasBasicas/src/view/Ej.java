public class Ej {
    public static void main(String[] args) {
        System.out.println("Mostrar los 10 primero multiplos de un numero");
        int num = 7;
        for (int i = 1; i <= 1; i++) {
            System.out.println(num + " x " + i + " = " + (num * i));
        }   
    }
}
